#include "fillit.h"

int		ft_sqrt(int nb)
{
	int i;
	int a;

	a = 1;
	i = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i <= nb / 2)
	{
		a = i * i;
		if (a == nb)
			return (i);
		i++;
	}
	return (ft_sqrt(nb + 1));
}
